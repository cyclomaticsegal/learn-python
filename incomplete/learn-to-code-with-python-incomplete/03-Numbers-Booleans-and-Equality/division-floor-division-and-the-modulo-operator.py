#returns a float  because division might return fractions
print(15 / 3)

#floor division - return integer and not rounded but chop off decimal
print(14 // 3)

#if neg then it will round down away from zero and then chop off
print(-14 // 3)

#modulo
print(14 % 3)
print(15 % 3)

#neg modulo results
print(1 % 3)
print(2 % 3)
print(3 % 3)
print(4 % 3)
